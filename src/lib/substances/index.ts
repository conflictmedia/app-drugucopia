/** All built-in substances, deduplicated by ID */
export const substances: Substance[] = Array.from(substanceMap.values()).sort(
  (a, b) => a.name.localeCompare(b.name),
);

/** Look up a built-in substance by its ID */
export function getSubstanceById(id: string): Substance | undefined {
  return substanceMap.get(id);
}

/** Get all built-in substances belonging to a given category */
export function getSubstancesByCategory(
  category: SubstanceCategory,
): Substance[] {
  return substances.filter((s) => s.categories.includes(category));
}

/** Get all substances including custom ones from localStorage */
export function getAllSubstances(): Substance[] {
  if (typeof window === 'undefined') return substances;
  try {
    const custom = JSON.parse(localStorage.getItem('drugucopia-custom-substances') || '[]') as Substance[];
    return [...substances, ...custom];
  } catch {
    return substances;
  }
}

/** Look up a substance by ID, including custom substances */
export function getSubstanceByIdAll(id: string): Substance | undefined {
  const builtin = substanceMap.get(id);
  if (builtin) return builtin;
  if (typeof window === 'undefined') return undefined;
  try {
    const custom = JSON.parse(localStorage.getItem('drugucopia-custom-substances') || '[]') as Substance[];
    return custom.find(s => s.id === id);
  } catch {
    return undefined;
  }
}

/** Search substances including custom ones */
export function searchSubstancesRankedAll(
  query: string,
  options: SearchOptions = {},
): SearchResult[] {
  if (typeof window === 'undefined') {
    return searchSubstancesRanked(query, options);
  }
  try {
    const custom = JSON.parse(localStorage.getItem('drugucopia-custom-substances') || '[]') as Substance[];
    const allSubs = [...substances, ...custom];
    
    // Reuse the search logic but with all substances
    const { limit = 20, minScore = 0, category, categoryFilter } = options;
    const cat = categoryFilter ?? category;
    const pool = cat ? allSubs.filter(s => s.categories.includes(cat)) : allSubs;
    
    // Simple search for custom substances
    if (!query || query.trim().length === 0) {
      return pool.slice(0, limit).map(s => ({
        substance: s,
        score: 0,
        matchField: 'name',
        matchType: 'name',
      }));
    }
    
    const normalize = (s: string) =>
      s.toLowerCase().replace(/[-–—]/g, ' ').replace(/\s+/g, ' ').trim();
    const q = normalize(query);
    const rawQ = query.toLowerCase().trim();
    
    const results: SearchResult[] = [];
    for (const substance of pool) {
      let bestScore = 0;
      let bestMatchField = 'name';
      const normId = normalize(substance.id);
      const normName = normalize(substance.name);
      const normClass = normalize(substance.class);
      
      if (normId === q) { bestScore = 1.0; bestMatchField = 'id'; }
      else if (normId.startsWith(q)) { bestScore = 0.95; bestMatchField = 'id'; }
      else if (normId.includes(q)) { bestScore = 0.85; bestMatchField = 'id'; }
      
      if (normName === q) {
        if (1.0 > bestScore) { bestScore = 1.0; bestMatchField = 'name'; }
      } else if (normName.startsWith(q)) {
        if (0.9 > bestScore) { bestScore = 0.9; bestMatchField = 'name'; }
      } else if (normName.includes(q)) {
        if (0.8 > bestScore) { bestScore = 0.8; bestMatchField = 'name'; }
      }
      
      for (const commonName of substance.commonNames) {
        const cn = normalize(commonName);
        if (cn === q) { if (0.88 > bestScore) { bestScore = 0.88; bestMatchField = commonName; } break; }
        if (cn.startsWith(q)) { if (0.78 > bestScore) { bestScore = 0.78; bestMatchField = commonName; } break; }
        if (cn.includes(q)) { if (0.65 > bestScore) { bestScore = 0.65; bestMatchField = commonName; } }
      }
      
      if (substance.aliases) {
        for (const alias of substance.aliases) {
          const a = normalize(alias);
          if (a === q) { if (0.87 > bestScore) { bestScore = 0.87; bestMatchField = alias; } break; }
          if (a.includes(q)) { if (0.6 > bestScore) { bestScore = 0.6; bestMatchField = alias; } }
        }
      }
      
      for (const cat of substance.categories) {
        if (cat.toLowerCase().includes(rawQ)) {
          if (0.4 > bestScore) { bestScore = 0.4; bestMatchField = 'category'; }
        }
      }
      
      if (normClass.includes(q)) {
        if (0.5 > bestScore) { bestScore = 0.5; bestMatchField = 'class'; }
      }
      
      if (substance.description.toLowerCase().includes(rawQ)) {
        if (0.3 > bestScore) { bestScore = 0.3; bestMatchField = 'description'; }
      }
      
      if (bestScore >= minScore) {
        results.push({
          substance,
          score: bestScore,
          matchField: bestMatchField,
          matchType: bestMatchField,
        });
      }
    }
    
    results.sort((a, b) => b.score - a.score || a.substance.name.localeCompare(b.substance.name));
    return results.slice(0, limit);
  } catch {
    return searchSubstancesRanked(query, options);
  }
}

// ─── SEARCH ──────────────────────────────────────────────────────────────────

export interface SearchOptions {
  /** Maximum number of results to return (default: 20) */
  limit?: number;
  /** Minimum score threshold (0-1) for inclusion (default: 0) */
  minScore?: number;
  /** Only search within the given category */
  category?: SubstanceCategory;
  /** Alias for category — used by interaction-substance-selector */
  categoryFilter?: SubstanceCategory;
}

export interface SearchResult {
  substance: Substance;
  score: number;
  /** The field that matched the query (e.g. 'name', 'commonName', 'class') */
  matchField: string;
  /** Alias kept for backwards compat; same value as matchField */
  matchType: string;
}

/**
 * Fuzzy search across substances with scoring.
 * Matches against id, name, commonNames, categories, class, and description.
 */
export function searchSubstancesRanked(
  query: string,
  options: SearchOptions = {},
): SearchResult[] {
  const {
    limit = 20,
    minScore = 0,
    category: categoryOpt,
    categoryFilter,
  } = options;
  const category = categoryFilter ?? categoryOpt;

  if (!query || query.trim().length === 0) {
    // Return all substances sorted by name when no query
    const pool = category ? getSubstancesByCategory(category) : substances;
    return pool.slice(0, limit).map((s) => ({
      substance: s,
      score: 0,
      matchField: "name",
      matchType: "name",
    }));
  }

  /**
   * Normalize a string for dash-insensitive matching.
   * Strips dashes/hyphens and collapses whitespace so that "4 ho met" matches
   * "4-ho-met", "4HO-MET", "4 h o met", etc. Also lowercases.
   */
  const normalize = (s: string) =>
    s.toLowerCase().replace(/[-–—]/g, " ").replace(/\s+/g, " ").trim();

  const q = normalize(query);
  const rawQ = query.toLowerCase().trim();
  const pool = category ? getSubstancesByCategory(category) : substances;
  const results: SearchResult[] = [];

  for (const substance of pool) {
    let bestScore = 0;
    let bestMatchField: string = "name";

    // Pre-normalize the comparison strings once per substance.
    const normId = normalize(substance.id);
    const normName = normalize(substance.name);
    const normClass = normalize(substance.class);

    // Exact ID match (highest priority)
    if (normId === q) {
      bestScore = 1.0;
      bestMatchField = "id";
    }
    // ID starts with query
    else if (normId.startsWith(q)) {
      bestScore = 0.95;
      bestMatchField = "id";
    }
    // ID contains query
    else if (normId.includes(q)) {
      bestScore = 0.85;
      bestMatchField = "id";
    }

    // Exact name match
    if (normName === q) {
      const score = 1.0;
      if (score > bestScore) {
        bestScore = score;
        bestMatchField = "name";
      }
    }
    // Name starts with query
    else if (normName.startsWith(q)) {
      const score = 0.9;
      if (score > bestScore) {
        bestScore = score;
        bestMatchField = "name";
      }
    }
    // Name contains query
    else if (normName.includes(q)) {
      const score = 0.8;
      if (score > bestScore) {
        bestScore = score;
        bestMatchField = "name";
      }
    }

    // Common names match
    for (const commonName of substance.commonNames) {
      const cn = normalize(commonName);
      if (cn === q) {
        const score = 0.88;
        if (score > bestScore) {
          bestScore = score;
          bestMatchField = commonName;
        }
        break;
      }
      if (cn.startsWith(q)) {
        const score = 0.78;
        if (score > bestScore) {
          bestScore = score;
          bestMatchField = commonName;
        }
        break;
      }
      if (cn.includes(q)) {
        const score = 0.65;
        if (score > bestScore) {
          bestScore = score;
          bestMatchField = commonName;
        }
      }
    }

    // Aliases match
    if (substance.aliases) {
      for (const alias of substance.aliases) {
        const a = normalize(alias);
        if (a === q) {
          const score = 0.87;
          if (score > bestScore) {
            bestScore = score;
            bestMatchField = alias;
          }
          break;
        }
        if (a.includes(q)) {
          const score = 0.6;
          if (score > bestScore) {
            bestScore = score;
            bestMatchField = alias;
          }
        }
      }
    }

    // Category match
    for (const cat of substance.categories) {
      if (cat.toLowerCase().includes(rawQ)) {
        const score = 0.4;
        if (score > bestScore) {
          bestScore = score;
          bestMatchField = "category";
        }
      }
    }

    // Chemical class match
    if (normClass.includes(q)) {
      const score = 0.5;
      if (score > bestScore) {
        bestScore = score;
        bestMatchField = "class";
      }
    }

    // Description contains query (lowest priority)
    if (substance.description.toLowerCase().includes(rawQ)) {
      const score = 0.3;
      if (score > bestScore) {
        bestScore = score;
        bestMatchField = "description";
      }
    }

    if (bestScore >= minScore) {
      results.push({
        substance,
        score: bestScore,
        matchField: bestMatchField,
        matchType: bestMatchField,
      });
    }
  }

  // Sort by score descending, then name ascending
  results.sort(
    (a, b) =>
      b.score - a.score || a.substance.name.localeCompare(b.substance.name),
  );

  return results.slice(0, limit);
} // Re-export types
